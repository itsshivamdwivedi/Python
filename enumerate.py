# Enumerate is a built in python function that allows you to loop- over sequenece and get the index of character at the same time

fruits=["Apple","Banana","Mango","Custard Apple","Orange"]
for index,fruit in enumerate(fruits):
    print(f"The {index} fruit is {fruit}")

